local function levelmaker(world)
    local levels = {}

    for map = 1, #raw_data do
        -- Decode the maps
        local raw_map = raw_data[map]
        for y = 1, #raw_map do
            for x = 1, #raw_map[y] do
                if raw_map[y][x] == 1 then
                    Tile(
                        (x - 1) * TILE_SIZE,
                        (y - 1) * TILE_SIZE,
                        world,
                        2)
                end
            end
        end
    end
end

